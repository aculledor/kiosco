    private int[] calcularHora(int hora, int minuto){
        int[] array = new int[2];
        if(minuto + 20 < 60){
            array[0] = hora;
            array[1] = minuto + 20;
        }else{
            array[0] = hora + 1;
            array[1] = minuto + 20 - 60;
        }
        return array;
    }
    
