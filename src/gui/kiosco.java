    private void masBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_masBtnActionPerformed
        // TODO add your handling code here:
        if(!(this.mTablaPedidos.getRowCount() > 0)){
            return;
        }
        selectedRow = this.mTablaPedidos.getSelectedRow();
        this.pedido.anhadirElemento(this.pedido.getProductos().get(this.mTablaPedidos.getSelectedRow()));
        this.tablaPedido.setFilas(this.pedido.getProductos());
        this.actualizarTotales();
        this.mTablaPedidos.setRowSelectionInterval(selectedRow, selectedRow);
    }//GEN-LAST:event_masBtnActionPerformed

    private void menosBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_menosBtnActionPerformed
        // TODO add your handling code here:
        if(!(this.mTablaPedidos.getRowCount() > 0)){
            return;
        }
        selectedRow = this.mTablaPedidos.getSelectedRow();
        this.pedido.disminuirElemento(this.pedido.getProductos().get(this.mTablaPedidos.getSelectedRow()));
        this.tablaPedido.setFilas(this.pedido.getProductos());
        this.actualizarTotales();
        this.mTablaPedidos.setRowSelectionInterval(selectedRow, selectedRow);
    }//GEN-LAST:event_menosBtnActionPerformed

    private void borrarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_borrarBtnActionPerformed
        // TODO add your handling code here:                                       
        // TODO add your handling code here:
        if(this.mTablaPedidos.getSelectedRow() < 0){
            return;
        }
        this.pedido.borrarElemento(this.pedido.getProductos().get(this.mTablaPedidos.getSelectedRow()));
        this.tablaPedido.setFilas(this.pedido.getProductos());
        this.actualizarTotales();
    }//GEN-LAST:event_borrarBtnActionPerformed

